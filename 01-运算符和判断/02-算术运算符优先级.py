# 在 Python 中，算术运算符的优先级决定了表达式中各个运算的计算顺序。优先级高的运算符会先被计算。如果多个运算符的优先级相同，Python 会按照从左到右的顺序依次计算。

"""
Python 算术运算符优先级（从高到低）

- 指数（幂运算）：**
- 正负号：+x、-x（一元运算符）
- 乘法、除法、取余、整除：*、/、%、//
- 加法、减法：+、-
"""

# 指数运算符 **

# 指数运算的优先级最高。它用于计算一个数的幂。

result = 2 ** 3 ** 2  # 先计算 3 ** 2 再计算 2 ** 9
print(result)  # 输出: 512

# 正负号 +x、-x（一元运算符）
# 正负号用于表示一个数的符号。它的优先级仅次于指数。

result = -3 ** 2  # 等价于 -(3 ** 2)
print(result)  # 输出: -9

# 注意：-3 ** 2 等价于 -(3 ** 2)，而不是 (-3) ** 2。

# 乘法、除法、取余、整除 *、/、%、//

# 这些运算符的优先级相同，它们会按从左到右的顺序依次计算。
result = 10 // 3 * 2 % 3
print(result)  # 输出: 2
# 解释: (10 // 3) -> 3, 然后 3 * 2 -> 6, 最后 6 % 3 -> 0

# 加法、减法 +、-

# 加法和减法的优先级最低，且它们的优先级相同。
result = 10 - 3 + 2
print(result)  # 输出: 9
# 解释: 先计算 10 - 3 -> 7, 然后 7 + 2 -> 9

# 使用括号改变运算顺序
#
# 如果想改变默认的运算顺序，可以使用括号 ()。括号内的表达式会被优先计算。
result = (2 + 3) * 4  # 括号使加法先计算
print(result)  # 输出: 20


result = 2 + 3 * 4 ** 2 // 5 - 6
# 解释: 先计算 4 ** 2 -> 16
# 接着 3 * 16 -> 48
# 然后 48 // 5 -> 9 (整除)
# 2 + 9 -> 11
# 最后 11 - 6 -> 5
print(result)  # 输出: 5
